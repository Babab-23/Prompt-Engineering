# AI Prompt Refinement: From Vague to Contextual

def show_prompts():
    print("=== AI Prompt Refinement Project ===\n")

    print("1. Vague Prompt:")
    print("Tell me about business.\n")

    print("Refined Prompt:")
    print("Explain the key functions of a small retail business in Nigeria, including marketing, finance, and customer service, in simple terms for a secondary school student.\n")

    print("Why it is better:")
    print("- Specifies location (Nigeria)")
    print("- Defines scope (retail business)")
    print("- Identifies audience")
    print("- Lists areas to cover\n")

    print("-" * 50)

    print("\n2. Vague Prompt:")
    print("Write a story.\n")

    print("Refined Prompt:")
    print("Write a short story (200–300 words) about a Nigerian student preparing for exams, including themes of hard work and perseverance.\n")

    print("Why it is better:")
    print("- Gives word count")
    print("- Defines context")
    print("- Adds theme guidance\n")

    print("-" * 50)

    print("\n3. Vague Prompt:")
    print("Explain photosynthesis.\n")

    print("Refined Prompt:")
    print("Explain photosynthesis in simple terms for a 12-year-old, including the role of sunlight, carbon dioxide, and chlorophyll.\n")

    print("Why it is better:")
    print("- Defines audience level")
    print("- Lists key concepts")
    print("- Improves clarity\n")

    print("-" * 50)

    print("\n4. Vague Prompt:")
    print("Give me math help.\n")

    print("Refined Prompt:")
    print("Help me solve a quadratic equation step-by-step, and explain each step clearly using an example like x^2 + 5x + 6 = 0.\n")

    print("Why it is better:")
    print("- Specifies topic")
    print("- Requests step-by-step explanation")
    print("- Includes example\n")

    print("-" * 50)

    print("\n5. Vague Prompt:")
    print("Tell me about football.\n")

    print("Refined Prompt:")
    print("Explain the basic rules of football (soccer), including number of players, scoring system, and duration of the match, for a beginner.\n")

    print("Why it is better:")
    print("- Clarifies type (soccer)")
    print("- Defines audience (beginner)")
    print("- Lists key points\n")

    print("-" * 50)

    print("\nConclusion:")
    print("Refined prompts are clearer, more specific, and provide context, which helps AI give better responses.")


if __name__ == "__main__":
    show_prompts()